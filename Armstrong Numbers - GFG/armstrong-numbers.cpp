class Solution {
  public:
    string armstrongNumber(int n){
        // code here
        int num=n;
        int ans=0;
        while(num!=0)
        {
            ans+=pow(num%10,3);
            num/=10;
        }
        if(ans==n)
        return "Yes";
        else
        return "No";
    }
};

